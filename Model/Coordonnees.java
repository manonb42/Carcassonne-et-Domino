package Model;

public class Coordonnees {
    private int X;
    private int Y;

    public Coordonnees(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }

    public int getX() {
        return X;
    }

    public void setX(int xx) {
        this.X = xx;
    }

    public int getY() {
        return Y;
    }

    public void setY(int yy) {
        this.Y = yy;
    }
}
